import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import postcss from "postcss";
import tailwindPostcss from "@tailwindcss/postcss";

const rootDir = join(dirname(fileURLToPath(import.meta.url)), "..");
const inputPath = join(rootDir, "src/styles/tailwind.css");
const css = readFileSync(inputPath, "utf8");
const result = await postcss([tailwindPostcss()]).process(css, { from: inputPath });
const outputPath = join(rootDir, "src/styles/reportStylesheet.ts");

writeFileSync(
    outputPath,
    `/* Generated by scripts/build-report-styles.mjs. Edit src/styles/tailwind.css and component class names instead. */\nexport const REPORT_STYLESHEET = ${JSON.stringify(result.css)};\n`,
    "utf8",
);

console.log(`Wrote ${outputPath} (${result.css.length} bytes)`);
