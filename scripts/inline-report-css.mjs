import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const rootDir = join(dirname(fileURLToPath(import.meta.url)), "..");
const cssFiles = [
    "src/theme/adaptive-tokens.css",
    "src/theme/stitchable-semantics.css",
    "src/components/report/report.css",
];
const bundledCss = cssFiles.map((relativePath) => readFileSync(join(rootDir, relativePath), "utf8")).join("\n");
const outputPath = join(rootDir, "src/components/report/reportStylesheet.ts");
const distCssDir = join(rootDir, "dist/components/report");
const distCssPath = join(distCssDir, "report.css");

writeFileSync(
    outputPath,
    `/* This file is generated by scripts/inline-report-css.mjs. Edit theme/report CSS sources instead. */\nexport const REPORT_STYLESHEET = ${JSON.stringify(bundledCss)};\n`,
    "utf8",
);

mkdirSync(distCssDir, { recursive: true });
writeFileSync(distCssPath, bundledCss, "utf8");
