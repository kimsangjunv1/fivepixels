export function pickReportSlice<T extends object, const K extends readonly (keyof T)[]>(source: T, keys: K): Pick<T, K[number]> {
    const out = {} as Pick<T, K[number]>;

    for (const key of keys) {
        out[key] = source[key];
    }

    return out;
}
